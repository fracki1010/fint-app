import { FileText, Truck, Receipt, LucideIcon } from "lucide-react";
import { VoucherType, VoucherStatus } from "../types/voucher";

/**
 * Get human-readable label for voucher type
 */
export function getVoucherTypeLabel(type: VoucherType): string {
  const labels: Record<VoucherType, string> = {
    invoice: "Factura",
    delivery_note: "Remito",
    receipt: "Recibo",
  };
  return labels[type];
}

/**
 * Get Lucide icon component for voucher type
 */
export function getVoucherTypeIcon(type: VoucherType): LucideIcon {
  const icons: Record<VoucherType, LucideIcon> = {
    invoice: FileText,
    delivery_note: Truck,
    receipt: Receipt,
  };
  return icons[type];
}

