/**
 * Format date to localized string
 */
export function formatVoucherDate(dateString: string): string {
  const date = new Date(dateString);
  return date.toLocaleDateString("es-AR", {
    day: "2-digit",
    month: "2-digit",
    year: "numeric",
    hour: "2-digit",
    minute: "2-digit",
  });
}

/**
 * Sort vouchers by type order (invoice first, then delivery_note, then receipt)
 */
export function sortVouchersByType<T extends { type: VoucherType }>(
  vouchers: T[]
): T[] {
  const typeOrder: Record<VoucherType, number> = {
    invoice: 1,
    delivery_note: 2,
    receipt: 3,
  };
  return [...vouchers].sort((a, b) => typeOrder[a.type] - typeOrder[b.type]);
}

/**
 * Check if receipt can be generated for an order
 * Receipts can only be generated for paid orders
 */
export function canGenerateReceipt(paymentStatus: string): boolean {
  return paymentStatus === "Pagado";
}

/**
 * Get default voucher types based on settings
 */
export function getDefaultVoucherTypes(
  settings?: {
    autoGenerateInvoice?: boolean;
    autoGenerateDeliveryNote?: boolean;
    autoGenerateReceipt?: boolean;
  } | null,
  paymentStatus?: string
): VoucherType[] {
  const defaults: VoucherType[] = [];

  if (settings?.autoGenerateInvoice) {
    defaults.push("invoice");
  }
  if (settings?.autoGenerateDeliveryNote) {
    defaults.push("delivery_note");
  }
  if (settings?.autoGenerateReceipt && paymentStatus === "Pagado") {
    defaults.push("receipt");
  }

  return defaults;
}
